package dto;

public class Student2DTO {
	int id ;
	String name; 
	String email;
	String phone;
	String major;
	String regdate; //날짜같은건 자바에서 String 으로 처리 
	double score;
	//생성자
	//밑에 public도 꼭 있어야 함 
	public Student2DTO
	(String name, String email, String phone, String major, double score) {
		super();
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.major = major;
		this.score = score;
	}
	
	
	public Student2DTO(int id, String name, String major) { //public 선언돼야 밖에서도 사용 가능 
		super(); //없어도 되는 super 
		this.id = id;
		this.name = name;
		this.major = major;
	}


	//id랑 regdate는 생성하지 않았음. 매개변수로 받은 5개만 
	//getter/ setter
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the major
	 */
	public String getMajor() {
		return major;
	}
	/**
	 * @param major the major to set
	 */
	public void setMajor(String major) {
		this.major = major;
	}
	/**
	 * @return the regdate
	 */
	public String getRegdate() {
		return regdate;
	}
	/**
	 * @param regdate the regdate to set
	 */
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	/**
	 * @return the score
	 */
	public double getScore() {
		return score;
	}
	/**
	 * @param score the score to set
	 */
	public void setScore(double score) {
		this.score = score;
	}
	@Override
	public String toString() {
		return  id +"-" + name +"-" + major +"-" ;
	}


	}

	//toString 필요시 구현 
	

